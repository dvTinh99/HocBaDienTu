package Model;

public class XepLop {
	int Ma_hs;
	int Ma_class;
	XepLop(){}
	public XepLop(int ma_hs, int ma_class) {
		super();
		Ma_hs = ma_hs;
		Ma_class = ma_class;
	}
	public int getMa_hs() {
		return Ma_hs;
	}
	public void setMa_hs(int ma_hs) {
		Ma_hs = ma_hs;
	}
	public int getMa_class() {
		return Ma_class;
	}
	public void setMa_class(int ma_class) {
		Ma_class = ma_class;
	}
	

}
